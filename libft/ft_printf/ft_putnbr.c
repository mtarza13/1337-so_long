#include "../libft.h"

int	ft_putnbr(int n)
{
	int	count;

	count = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		count += 1;
		n = -n;
	}
	if (n >= 10)
		count += ft_putnbr(n / 10);
	n = (n % 10) + '0';
	write(1, &n, 1);
	count += 1;
	return (count);
}
